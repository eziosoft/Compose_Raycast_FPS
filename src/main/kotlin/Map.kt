fun generateMap(w: Int, h: Int): IntArray {
    val map = IntArray(w * h) { 0 }
    for (y in 0 until h) {
        for (x in 0 until w) {
            if (x == 0 || y == 0 || x == w - 1 || y == h - 1) {
                map[y * w + x] = 2
            } else {
                map[y * w + x] = if (Math.random() < 0.2) 1 else 0
            }

            if (x == 1 && y == 1) {
                map[y * w + x] = 0
            }
        }
    }
    return map
}

private fun drawPlayerOnMap(
    bitmap: Screen,
    player: Player,
    xOffset: Int,
    yOffset: Int,
    color: Screen.Color = Screen.Color(0, 255, 255),
    playerSize: Float
) {
    bitmap.drawFilledRect(
        xOffset + (player.x - playerSize / 2).toInt(),
        yOffset + (player.y - playerSize / 2).toInt(),
        playerSize.toInt(),
        playerSize.toInt(),
        color
    )
    bitmap.drawLine(
        xOffset + player.x.toInt(),
        yOffset + player.y.toInt(),
        (xOffset + player.x + cos(player.rotationRad.toDouble()).toFloat() * playerSize * 2).toInt(),
        (yOffset + player.y + sin(player.rotationRad.toDouble()).toFloat() * playerSize * 2).toInt(),
        color
    )
}

fun drawMap(
    bitmap: Screen,
    xOffset: Int,
    yOffset: Int,
    cellSize: Int,
    player: Player,
    playerSize: Float,
    enemies: List<Player>
) {
    for (y: Int in 0 until MAP_Y) {
        for (x in 0 until MAP_X) {

            val color: Screen.Color = if (MAP[y * MAP_X + x] > 0) {
                Screen.Color(255, 255, 255)
            } else {
                Screen.Color(0, 0, 0)
            }

            bitmap.drawFilledRect(xOffset + x * cellSize, yOffset + y * cellSize, cellSize, cellSize, color)
        }
    }

    drawPlayerOnMap(
        bitmap,
        player,
        xOffset = xOffset,
        yOffset = yOffset,
        color = Screen.Color(0, 255, 255),
        playerSize = playerSize
    )

    enemies.forEach { enemy ->
        drawPlayerOnMap(
            bitmap,
            enemy,
            xOffset = xOffset,
            yOffset = yOffset,
            color = Screen.Color(255, 0, 0),
            playerSize = playerSize
        )
    }
}